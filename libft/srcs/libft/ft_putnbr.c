#include "libft.h"

void	ft_putnbr(int nb)
{
	long	n;

	n = nb;
	(n < 0 && (n = -n) ? ft_putchar('-') : n);
	(n >= 10) ? ft_putnbr(n / 10) : n;
	ft_putchar(n % 10 + '0');
}
